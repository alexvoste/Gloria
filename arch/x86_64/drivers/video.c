void video_init(struct limine_framebuffer *fb) {
  g_fb = fb;
  g_pitch_div = fb->pitch / (fb->bpp / 8);
}

void video_put_pixel(int x, int y, uint32_t color) {
  if (!g_fb || x < 0 || y < 0 || x >= (int)g_fb->width ||
      y >= (int)g_fb->height)
    return;
  uint32_t *fb_ptr = (uint32_t *)((uintptr_t)g_fb->address + y * g_fb->pitch +
                                  x * (g_fb->bpp / 8));
  *fb_ptr = color;
}

void video_clear(uint32_t color) {
  if (!g_fb)
    return;
  for (uint32_t y = 0; y < g_fb->height; y++) {
    for (uint32_t x = 0; x < g_fb->width; x++) {
      video_put_pixel(x, y, color);
    }
  }
}

void video_draw_rect(int x, int y, int width, int height, uint32_t color) {
  for (int i = 0; i < width; i++) {
    video_put_pixel(x + i, y, color);
    video_put_pixel(x + i, y + height - 1, color);
  }
  for (int i = 0; i < height; i++) {
    video_put_pixel(x, y + i, color);
    video_put_pixel(x + width - 1, y + i, color);
  }
}

void video_draw_line(int x0, int y0, int x1, int y1, uint32_t color) {
  int dx = x1 - x0;
  int dy = y1 - y0;
  int sx = dx > 0 ? 1 : -1;
  int sy = dy > 0 ? 1 : -1;
  dx = dx < 0 ? -dx : dx;
  dy = dy < 0 ? -dy : dy;
  int err = dx - dy;

  while (1) {
    video_put_pixel(x0, y0, color);
    if (x0 == x1 && y0 == y1)
      break;
    int e2 = 2 * err;
    if (e2 > -dy) {
      err -= dy;
      x0 += sx;
    }
    if (e2 < dx) {
      err += dx;
      y0 += sy;
    }
  }
}

void video_putchar(int x, int y, char c, uint32_t color) {
  if (!g_fb || (unsigned char)c >= 128)
    return;
  for (int row = 0; row < 8; row++) {
    uint8_t line = font[(uint8_t)c][row];
    for (int col = 0; col < 8; col++) {
      if (line & (1 << (7 - col))) {
        video_put_pixel(x + col, y + row, color);
      }
    }
  }
}

void video_print(int x, int y, const char *str, uint32_t color) {
  int cx = x;
  int cy = y;
  while (*str) {
    if (*str == '\n') {
      cx = x;
      cy += 10;
    } else {
      video_putchar(cx, cy, *str, color);
      cx += 8;
    }
    str++;
  }
}
